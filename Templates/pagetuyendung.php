                            - Ưu tiên có kinh nghiệm ở vị trí tương đương tại các nhà máy sản xuất đạt tiêu chuẩn GMP<br>
                            - Phẩm chất: Sức khỏe tốt; cẩn thận, tỉ mỉ, chu đáo, nhanh nhẹn, chuyên cần, có trách nhiệm trong công việc.</p>

