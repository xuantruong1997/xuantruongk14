                            <p><strong>1. VỊ TRÍ: NHÂN VIÊN KỸ THUẬT XƯỞNG SẢN XUẤT</strong></p>

                            <p><strong>2. ĐƠN VỊ CẦN TUYỂN:</strong> Nhà máy Dược phẩm số 2.</p>

                            <p><strong>3. NƠI LÀM VIỆC:</strong> Tổ 5, Phường Quang Vinh, Thành phố Thái Nguyên, Tỉnh Thái Nguyên, Việt Nam.</p>

                            <p><strong>4. SỐ LƯỢNG:</strong> 01 người</p>

                            <p><strong>5. MỨC LƯƠNG:</strong>&nbsp;10.000.000 đồng</p>

                            <p><strong>6. MÔ TẢ CÔNG VIỆC:</strong></p>

                            <p><strong>6.1. THEO DÕI QUÁ TRÌNH SẢN XUẤT: </strong><br>
                            - Với QTSX đang ban hành:<br>
                            + Theo dõi sự phù hợp của qui trình về: thành phần nguyên liệu, cách thức xử lý nguyên liệu, các bước sản xuất, thông số, yêu cầu chất lượng và kiểm soát.&nbsp;<br>
                            + Theo dõi và đánh giá những thay đổi trong quá trình sản xuất; để đề nghị xem xét và điều chỉnh QTSX.<br>
                            + Quyết định khối lượng phân liều của BTP khi có yêu cầu của BQĐ.<br>
                            - Với QTSX mới<br>
                            + Theo dõi quá trình triển khai QTSX mới của P.NCPT với nhà máy.<br>
                            + Tham gia đánh giá về tính khả thi của QTSX mới với dây truyền thiết bị, hiệu suất và định mức lao động.<br>
                            + Đề xuất cho việc áp dụng QTSX mới: đạt hoặc cần điều chỉnh.<br>
                            + Trực tiếp theo dõi quá trình sản xuất các lô sản phẩm tiếp theo để đảm bảo các thông số trong QTSX mới được áp dụng<br>
                            - Xây dựng và triển khai QTSX theo yêu cầu<br>
                            + Soạn thảo và phối hợp soạn thảo QTSX<br>
                            + Triển khai QTSX<br>
                            + Thay đổi quy trình cho phù hợp với các đánh giá thực tế.<br>
                            - Theo dõi nguyên phụ liệu<br>
                            + Xác định đặc tính của mỗi nguyên phụ liệu phù hợp với từng dạng bào chế, sản phẩm.&nbsp;<br>
                            + Tư vấn với P.NCPT hoặc P.KHVT trong việc mua nguyên liệu phù hợp.<br>
                            + Đánh giá và báo cáo với BQĐ, các phòng ban liên quan khi nguyên liệu cung ứng có sự sai khác.<br>
                            + Theo dõi sự phù hợp với QTSX đang ban hành với nguyên phụ liệu có sự sai khác được chấp nhận đưa vào sản xuất.</p>

                            <p><strong>6.2. XỬ LÝ BTP CÓ SỰ CỐ VỀ CHẤT LƯỢNG:</strong><br>
                            - Cùng BQĐ, P.ĐBCL (khi cần) để đánh giá, xem xét và đưa ra các phương án xử lý.<br>
                            - Phối hợp với các tổ sản xuất triển khai các phương án xử lý: mẫu thử, bố trí thiết bị thử nghiệm, liên hệ P.KTCL để kiểm tra các chỉ tiêu theo yêu cầu.<br>
                            - Đánh giá dựa trên kết quả các mẫu thử đề xuất với BQĐ để quyết định phương án xử lý hợp lý về chất lượng, nhân công, hư hao.<br>
                            - Ghi nhận các nguyên nhân dẫn đến sai lệch của BTP để tránh lặp lại cho các lần sản xuất sau. Đề xuất các biện pháp phòng ngừa.</p>

                            <p><strong>6.3. HỒ SƠ TÀI LIỆU:</strong><br>
                            - Tham gia soạn thảo, chỉnh sửa qui trình, qui định, hướng dẫn làm việc và hồ sơ biểu mẫu theo yêu cầu của BQĐ.<br>
                            - Tham gia công tác thanh tra, thẩm định GMP.</p>

                            <p><strong>6.4. ĐÀO TẠO:</strong><br>
                            - Soạn thảo tài liệu đào tạo.<br>
                            - Đào tạo nhân viên mới theo yêu cầu của BQĐ.&nbsp;<br>
                            - Tham gia đào tạo các vấn đề kỹ thuật trong sản xuất định kỳ hoặc đột xuất.</p>

                            <p><strong>6.5. BÁO CÁO KỸ THUẬT:</strong><br>
                            - Thực hiện báo cáo định kỳ hàng tháng hoặc khi được yêu cầu.</p>

                            <p><strong>7. QUYỀN LỢI:</strong><br>
                            - Mức lương: Thỏa thuận. Sẽ trao đổi trong quá trình phỏng vấn.<br>
                            - Được hưởng mọi chế độ và quyền lợi theo đúng quy định của Luật Lao động.<br>
                            - Được tham gia nhiều chương trình đào tạo và phát triển bản thân.<br>
                            - Được hưởng chế độ tham quan, nghỉ mát hàng năm của Công ty</p>

                            <p><strong>8. YÊU CẦU CÔNG VIỆC:</strong><br>
                            - Giới tính: Nam, Nữ<br>
                            - Trình độ: Đại học Dược Hà Nội hoặc tương đương.<br>
